temperature = 60

if temperature >= 45 and temperature <= 85:
    print("There is no worry about the cold nor the heat. Yet, bring a light jacket and some water.")

elif temperature < 45:
    print("It's frigid out, please bring a heavier jacket with a hat, a scarf, and some gloves out with you.")

else:
    print("It's hot out, please bring some shades, a sunscreen lotion, and a water bottle with you.")
